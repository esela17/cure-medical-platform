def generate():
    md = markdown.Markdown(extensions=['tables', 'nl2br'])
    catalog = []
    
    # Load existing catalog to preserve article images if needed
    if os.path.exists('articles_catalog.json'):
        with open('articles_catalog.json', 'r', encoding='utf-8') as f:
            catalog = json.load(f)

    print(f"Generating {len(MISSING_ARTICLES)} missing articles...")
    
    for idx, art in enumerate(MISSING_ARTICLES):
        html_content = md.convert(art['content'])
        # Add table wrapper
        html_content = html_content.replace('<table>', '<div class="table-wrapper"><table>').replace('</table>', '</table></div>')
        
        # Image cycling (continue from 1-6)
        # Using index + 13 because these start after 12
        img_id = ((idx + 12) % 6) + 1
        img_filename = f"article{img_id}.png"
        
        page_html = TEMPLATE.replace('{{TITLE}}', art['title'])\
                            .replace('{{META_DESC}}', art['desc'])\
                            .replace('{{SLUG}}', art['slug'])\
                            .replace('{{IMG}}', img_filename)\
                            .replace('{{CONTENT}}', html_content)
        
        with open(f"{art['slug']}.html", 'w', encoding='utf-8') as f:
            f.write(page_html)
            
        print(f"[OK] Generated {art['slug']}.html")
        
        # Check if already in catalog
        if not any(a['slug'] == art['slug'] for a in catalog):
            catalog.append({
                'title': art['title'],
                'slug': art['slug'],
                'desc': art['desc'],
                'image': img_filename
            })

    # Save final catalog
    with open('articles_catalog.json', 'w', encoding='utf-8') as f:
        json.dump(catalog, f, ensure_ascii=False, indent=2)
    
    print("\nAll 25 articles are now generated and cataloged.")

if __name__ == "__main__":
    generate()
